package com.example.pl.model;

public class LoginPegawaiModel {
    private String nip, username, pass, level_pegawai, nama_divisi;

    public String getNip() {
        return nip;
    }

    public void setNip(String nip) {
        this.nip = nip;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getLevel_pegawai() {
        return level_pegawai;
    }

    public void setLevel_pegawai(String level_pegawai) {
        this.level_pegawai = level_pegawai;
    }

    public String getNama_divisi() {
        return nama_divisi;
    }

    public void setNama_divisi(String nama_divisi) {
        this.nama_divisi = nama_divisi;
    }
}
